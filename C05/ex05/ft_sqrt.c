int	ft_sqrt(int nb)
{
	int	n;
	int	sqrt;

	n = 1;
	sqrt = 0;
	if (nb <= 0 || nb > 2147483647)
		return (0);
	if (nb == 1)
		return (1);
	while (n <= nb / 2)
	{
		sqrt = n * n;
		if (sqrt == nb)
			return (n);
		n++;
	}
	return (0);
}
